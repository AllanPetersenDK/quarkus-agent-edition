package dk.ashlan.agent.eval;

import org.eclipse.microprofile.openapi.annotations.media.Schema;

import java.util.List;

public record Chapter10EvalCaseResult(
        @Schema(description = "Case identifier.")
        String caseId,
        @Schema(description = "Target lane used for the case.")
        String targetLane,
        @Schema(description = "Runtime run identifier generated by the evaluated lane.")
        String runId,
        @Schema(description = "Case input.")
        String input,
        @Schema(description = "Observed output or answer.")
        String output,
        @Schema(description = "True when the case passed.")
        boolean passed,
        @Schema(description = "Lightweight evaluation score between 0 and 1.")
        double score,
        @Schema(description = "Human-readable explanation of the evaluation result.")
        String explanation,
        @Schema(description = "Failure reason when the case did not pass.")
        String failureReason,
        @Schema(description = "Observed signals or trace markers used to make the judgement.")
        List<String> observedSignals,
        @Schema(description = "Selected trace excerpts.")
        List<String> selectedTraceEntries,
        @Schema(description = "Compact trace summary.")
        String traceSummary
) {
    public Chapter10EvalCaseResult {
        observedSignals = observedSignals == null ? List.of() : List.copyOf(observedSignals);
        selectedTraceEntries = selectedTraceEntries == null ? List.of() : List.copyOf(selectedTraceEntries);
    }
}
